numbers = [1, 2, 3, 4, 5]
#The append() method appends an element to the end of the list.
numbers.append(6)
print(numbers)
#The list insert() method inserts an element to the list at the specified index.
numbers.insert(0, -1)
print(numbers)
#Remove() searches for the given element in the list and removes the first matching elemen
numbers.remove(3)
print(numbers)
#The clear() method removes all items from the list.
numbers.clear()
print(numbers)


